#Starting in Python 3.6 you can use underscores to write large numbers in a more readable way
12_300_000 #This is the same as 12,300,000
# scientific notation
# 3e4 =3E4 = 3 * 10000 = 30000
# 3e-4 = 3E-4 = 3 * 1/10000 = 0.0003
print(0.000000000000000000000000003)
# octal numbers
# start with 0O or 0o
print(0o123)
# hexadecimal numbers 
# start with 0X or 0x
print(0x123)